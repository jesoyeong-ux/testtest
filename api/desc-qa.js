/* ── 메인 Q&A 생성 함수 ─────────────────────── */

/**
 * 책 소개 기반으로 책별 맞춤 Q&A 2개 생성
 * @param {object} book - { title, author, _rawDescription, ... }
 * @param {string} sectionKey - 'essay' | 'novel' | 'selfdev' | 'bonus'
 * @returns {Array<{q: string, a: string}>}
 */
function generateDescriptionQA(book, sectionKey) {
  const title  = book.title  || '';
  const author = book.author || '';
  const desc   = book._rawDescription || '';
  const month  = new Date().getMonth() + 1;
  const season = SEASON_TREND[month] || SEASON_TREND[5];
  const kp     = extractKeyPhrase(desc);
  const tkp    = truncateKP(kp, 38, title); // 단어 경계 자름 + 제목 동일 방지
  const themes = detectThemes(desc);

  // 섹션 특성에 맞는 테마 우선순위 적용
  const priority = SECTION_THEME_PRIORITY[sectionKey] || SECTION_THEME_PRIORITY.bonus;

  for (const theme of priority) {
    if (themes.includes(theme) && THEME_QA[theme]) {
      const variants = THEME_QA[theme];
      const vi = titleHash(title) % variants.length;
      const pair = variants[vi];
      return pair.map(item => ({
        q: typeof item.q === 'function' ? item.q(title, author, tkp, season) : item.q,
        a: typeof item.a === 'function' ? item.a(title, author, tkp, season) : item.a,
      }));
    }
  }

  // 테마 미감지 → 섹션별 fallback
  const fallback = SECTION_FALLBACK[sectionKey] || SECTION_FALLBACK.bonus;
  const vi = titleHash(title) % fallback.length;
  const pair = fallback[vi];
  return pair.map(item => ({
    q: typeof item.q === 'function' ? item.q(title, author, tkp, season) : item.q,
    a: typeof item.a === 'function' ? item.a(title, author, tkp, season) : item.a,
  }));
}

/**
 * 섹션 내 모든 책에 설명 기반 Q&A 적용
 * (openai-qa.js 의 enrichBooksWithQA 와 동일 인터페이스)
 */
function enrichBooksWithDescQA(books, sectionKey) {
  return books.map(book => {
    try {
      const qa = generateDescriptionQA(book, sectionKey);
      if (qa && qa.length) return { ...book, qa };
    } catch (e) {
      // 실패 시 기존 템플릿 Q&A 유지
    }
    return book;
  });
}

