/* ── 유틸 함수 ───────────────────────────────── */

function titleHash(str) {
  let h = 0;
  for (let i = 0; i < str.length; i++) {
    h = (Math.imul(31, h) + str.charCodeAt(i)) | 0;
  }
  return Math.abs(h);
}

/** 강한 키워드 1개 이상, 또는 약한 키워드 2개 이상일 때 테마 감지 */
function detectThemes(desc) {
  if (!desc) return [];
  const themes = [];
  for (const [theme, { strong, weak }] of Object.entries(THEME_KEYWORDS)) {
    const hasStrong = strong.some(kw => desc.includes(kw));
    const weakCount = weak.filter(kw => desc.includes(kw)).length;
    if (hasStrong || weakCount >= 2) themes.push(theme);
  }
  return themes;
}

/* 홍보성/메타 문장 패턴 (책 소개에서 제외) */
const PROMO_PATTERNS = [/★/, /◆/, /●/, /▶/, /◀/, /〈.+〉/, /《.+》/, /≪.+≫/, /개봉/, /출판/, /추천도서/, /베스트셀러/, /수상/, /^\d{4}년/, /^◎/, /^\*/];

/** 책 소개에서 핵심 구절 추출 (홍보 문구 제외, 실제 내용 우선) */
function extractKeyPhrase(desc) {
  if (!desc || desc.length < 5) return '';
  // 따옴표 안 구절 우선 (저자의 직접 메시지) — 인용 부호 제거
  const quoted = desc.match(/[""『「]([^""』」]{10,45})[""』」]/);
  if (quoted) return quoted[1].replace(/^["'"'"「『《≪]+|["'"'"」』》≫]+$/g, '').trim();
  // 홍보성 문장 제외, 실제 내용 문장 찾기
  const sentences = desc.split(/[.。!?\n]\s*/);
  for (const s of sentences) {
    const clean = s.replace(/["""''★◆●▶◀◎*]/g, '').trim();
    if (clean.length < 15 || clean.length > 65) continue;
    if (PROMO_PATTERNS.some(p => p.test(clean))) continue;
    return clean;
  }
  // fallback: 전체 설명 앞부분 (특수문자 제거)
  const stripped = desc.replace(/[★◆●▶◀\*◎"'"'""]/g, '').trim();
  return stripped.slice(0, 45).trim();
}

/** 단어 경계에서 자르기 + 책 제목과 동일하면 빈 값 반환 */
function truncateKP(kp, maxLen = 38, title = '') {
  if (!kp) return '';
  // 책 제목 자체가 키프레이즈로 추출된 경우 무시
  if (title && (kp.includes(title.slice(0, 6)) || title.includes(kp.slice(0, 6)))) return '';
  if (kp.length <= maxLen) return kp;
  const cut = kp.slice(0, maxLen);
  const lastSpace = cut.lastIndexOf(' ');
  return lastSpace > maxLen / 2 ? cut.slice(0, lastSpace) : cut;
}

